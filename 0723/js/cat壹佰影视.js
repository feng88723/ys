async function detail(id) {
    const dUrl = host + id;
    const dResp = await req(dUrl, {headers});
    const dhtml = dResp.content;
    
    // 提取基本信息
    const playFrom = [], playList = [];
    const tabs = pdfa(dhtml, '.playfrom.tab8 li');
    const playBoxes = pdfa(dhtml, '.playlist .videourl');
    
    for (let i = 0; i < Math.min(tabs.length, playBoxes.length); i++) {
        const name = pdfh(tabs[i], 'li&&Text') || '播放源' + (i+1);
        const episodes = pdfa(playBoxes[i], 'li a')
            .map(a => pdfh(a, 'a&&Text') + '$' + pdfh(a, 'a&&href'))
            .join('#');
        if (episodes) {
            playFrom.push(name);
            playList.push(episodes);
        }
    }
    
    return JSON.stringify({
        list: [{
            vod_id: id,
            vod_name: pdfh(dhtml, 'dt.name&&Text') || pdfh(dhtml, '.title a:last-child&&Text'),
            vod_pic: pdfh(dhtml, '.ct-l img&&src') || pdfh(dhtml, '.ct-l img&&data-original'),
            vod_year: pdfh(dhtml, 'dd:contains(年份)&&Text').replace('年份：', '') || '',
            vod_area: pdfh(dhtml, 'dd:contains(地区)&&Text').replace('地区：', '') || '',
            vod_remarks: pdfh(dhtml, 'dd:contains(备注)&&Text').replace('备注：', '') || '',
            type_name: pdfh(dhtml, 'dt:contains(类型)&&Text').replace('类型：', '') || '',
            vod_actor: pdfh(dhtml, 'dt:contains(主演)&&Text').replace('主演：', '') || '',
            vod_director: pdfh(dhtml, 'dd:contains(导演)&&Text').replace('导演：', '') || '',
            vod_content: pdfh(dhtml, '.tab-jq&&Text') || '',
            vod_play_from: playFrom.join('$$$'),
            vod_play_url: playList.join('$$$')
        }]
    });
}

