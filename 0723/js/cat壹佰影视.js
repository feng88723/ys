function getList(html) {
    let videos = [];
    const list = pdfa(html, '.link-hover');
    list.forEach(it => {
        const id = pdfh(it, 'a&&href');
        const name = pdfh(it, 'a&&title');
        const pic = pdfh(it, 'img&&src');
        const remark = pdfh(it, '.other&&Text');
        videos.push({
            vod_id: id,
            vod_name: name,
            vod_pic: pic,
            vod_remarks: remark
        });
    });
    return videos;
}

